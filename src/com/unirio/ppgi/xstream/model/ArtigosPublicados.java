package com.unirio.ppgi.xstream.model;

import java.util.List;

import com.thoughtworks.xstream.annotations.XStreamAlias;
import com.thoughtworks.xstream.annotations.XStreamImplicit;

@XStreamAlias("ARTIGOS-PUBLICADOS")
public class ArtigosPublicados {

	// ARTIGOS-PUBLICADO
	@XStreamImplicit(itemFieldName = "ARTIGO-PUBLICADO")
	private List<ArtigoPublicado> artigoPublicado;

	public List<ArtigoPublicado> getArtigoPublicado() {
		return artigoPublicado;
	}

	public void setArtigoPublicado(List<ArtigoPublicado> artigoPublicado) {
		this.artigoPublicado = artigoPublicado;
	}

}
